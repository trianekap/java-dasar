public class operasiMatematika {

    public static void main(String[] args) {
        
        int a = 100;
        int b = 50;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        int c = 10;

        c += 10;
        System.out.println(c);

        c -= 10;
        System.out.println(c);

        c *=10;
        System.out.println(c);

        c /=10;
        System.out.println(c);

        int d = 15;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);

        ++d;
        System.out.println(d);

        --d;
        System.out.println(d);

    }
}